# 2-4. Name Case:
##############################
#       Use a variable to represent a person’s name, and then print
#   that person’s name in lowercase, uppercase, and title case.

name = input("Enter your Name: ")

print("\n\nYour Name is:" + name + "\n")

print("Lowercase name:" + name.lower() + "\n")

print("Uppercase name:" + name.upper()+ "\n")

print("Title case name:" + name.title()+ "\n")
